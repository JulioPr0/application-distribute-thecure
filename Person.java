public class Person {
    private String dateBirth;
    private String fullName;

    public Person(String dateBirth, String fullName) {
        this.dateBirth = dateBirth;
        this.fullName = fullName;
    }

    public String getdateBirth() {
        return dateBirth;
    }

    public String getFullName() {
        return fullName;
    }
}
